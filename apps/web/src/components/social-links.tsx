// X / GitHub / Instagram の公式マークのみを表示し、SDK経由で外部リンクを開く。
export default function SocialLinks({ twitterUrl, githubUrl, instagramUrl, emptyText }: {
  twitterUrl: string | null;
  githubUrl: string | null;
  instagramUrl?: string | null;
  emptyText?: string;
}) {
  if (!twitterUrl && !githubUrl && !instagramUrl) {
    return emptyText ? <p className="text-xs" style={{ color: "var(--muted)" }}>{emptyText}</p> : null;
  }
  return (
    <div className="flex gap-2">
      {twitterUrl && (
        <button onClick={() => openExternalLink(twitterUrl)} aria-label="X"
          className="w-9 h-9 rounded-full flex items-center justify-center transition-transform active:scale-95"
          style={{ background: "#000", color: "#fff" }}>
          <XMark />
        </button>
      )}
      {githubUrl && (
        <button onClick={() => openExternalLink(githubUrl)} aria-label="GitHub"
          className="w-9 h-9 rounded-full flex items-center justify-center transition-transform active:scale-95"
          style={{ background: "#1b1f24", color: "#fff" }}>
          <GithubMark />
        </button>
      )}
      {instagramUrl && (
        <button onClick={() => openExternalLink(instagramUrl)} aria-label="Instagram"
          className="w-9 h-9 rounded-full flex items-center justify-center transition-transform active:scale-95"
          style={{ background: "linear-gradient(45deg, #feda75, #fa7e1e, #d62976, #962fbf, #4f5bd5)", color: "#fff" }}>
          <InstagramMark />
        </button>
      )}
    </div>
  );
}
